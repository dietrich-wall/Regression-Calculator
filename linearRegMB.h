#include <windows.h>


////////////////////////////////////////////////////////////////////////////////////////////
/// Update user about satus and wait for acknowledgement 
////////////////////////////////////////////////////////////////////////////////////////////

void MessageBox_OK(char *message, char *messageType){

	MessageBox(NULL, message, messageType, MB_OK);
}

////////////////////////////////////////////////////////////////////////////////////////////
/// Get yes or no selection ID 
////////////////////////////////////////////////////////////////////////////////////////////

int MessageBox_YESNO(char *message, char *messageType){
	
	int selection = MessageBox(NULL, message, messageType, MB_YESNO);
	
	return selection;
}
